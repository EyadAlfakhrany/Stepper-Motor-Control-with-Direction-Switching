void STEPMOTOR(uint8 direction)
{switch(direction)
	{case CLOCK_WISE :
DIO_WritePort(PORTB_PIN0,0x09);
_delay_ms(5);
DIO_WritePort(PORTB_PIN0,0x03);
_delay_ms(5);
DIO_WritePort(PORTB_PIN0,0x06);
_delay_ms(5);
DIO_WritePort(PORTB_PIN0,0x0c);
_delay_ms(5);
break;

case COUNTER_CLOCK_WISE :
DIO_WritePort(PORTB_PIN0,0x0c);
_delay_ms(2);
DIO_WritePort(PORTB_PIN0,0x06);
_delay_ms(2);
DIO_WritePort(PORTB_PIN0,0x03);
_delay_ms(2);
DIO_WritePort(PORTB_PIN0,0x09);
_delay_ms(2);
break;
}
}

